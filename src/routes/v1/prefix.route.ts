import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { prefixValidation } from '../../validations';
import { prefixController } from '../../controllers';
import authSession from '../../middlewares/authSession';

const router = express.Router();

router.use(authSession())
  .route('/')
  .get(
    auth('getPrefixes'),
    validate(prefixValidation.getPrefixes),
    prefixController.getPrefixes
  );

router.use(authSession())
  .route('/:unitId')
  .patch(
    auth('managePrefixes'),
    validate(prefixValidation.updatePrefix),
    prefixController.updatePrefix
  );

export default router;

