import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { instituteValidation } from '../../validations';
import { instituteController } from '../../controllers';

const router = express.Router();

router
  .route('/')
  .post(auth('manageInstitutes'), validate(instituteValidation.createInstitute), instituteController.createInstitute)
  .get(auth('getInstitutes'), validate(instituteValidation.getInstitutes), instituteController.getInstitutes);

router
  .route('/:instituteId')
  .get(auth('getInstitutes'), validate(instituteValidation.getInstitute), instituteController.getInstitute)
  .patch(auth('manageInstitutes'), validate(instituteValidation.updateInstitute), instituteController.updateInstitute)
  .delete(auth('manageInstitutes'), validate(instituteValidation.deleteInstitute), instituteController.deleteInstitute);

export default router;

