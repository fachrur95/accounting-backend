import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { peopleValidation } from '../../validations';
import { peopleController } from '../../controllers';
import authSession from '../../middlewares/authSession';
import multerUpload from '../../middlewares/multer';

const router = express.Router();

router.use(authSession())
  .route('/')
  .post(
    auth('managePeoples'),
    multerUpload.array("files"),
    validate(peopleValidation.createPeople),
    peopleController.createPeople
  )
  .get(
    auth('getPeoples'),
    validate(peopleValidation.getPeoples),
    peopleController.getPeoples
  );

router.use(authSession())
  .route('/:peopleId')
  .get(
    auth('getPeoples'),
    validate(peopleValidation.getPeople),
    peopleController.getPeople
  )
  .patch(
    auth('managePeoples'),
    validate(peopleValidation.updatePeople),
    peopleController.updatePeople
  )
  .delete(
    auth('managePeoples'),
    validate(peopleValidation.deletePeople),
    peopleController.deletePeople
  );

export default router;

