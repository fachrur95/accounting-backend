import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { itemTypeValidation } from '../../validations';
import { itemTypeController } from '../../controllers';

const router = express.Router();

router
  .route('/')
  .post(
    auth('manageItemTypes'),
    validate(itemTypeValidation.createItemType),
    itemTypeController.createItemType
  )
  .get(
    auth('getItemTypes'),
    validate(itemTypeValidation.getItemTypes),
    itemTypeController.getItemTypes
  );

router
  .route('/:itemTypeId')
  .get(
    auth('getItemTypes'),
    validate(itemTypeValidation.getItemType),
    itemTypeController.getItemType
  )
  .patch(
    auth('manageItemTypes'),
    validate(itemTypeValidation.updateItemType),
    itemTypeController.updateItemType
  )
  .delete(
    auth('manageItemTypes'),
    validate(itemTypeValidation.deleteItemType),
    itemTypeController.deleteItemType
  );

export default router;

