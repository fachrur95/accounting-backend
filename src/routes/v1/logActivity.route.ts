import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { logActivityValidation } from '../../validations';
import { logActivityController } from '../../controllers';
import authSession from '../../middlewares/authSession';

const router = express.Router();

router.use(authSession())
  .route('/')
  .get(
    auth('getLogActivities'),
    validate(logActivityValidation.getLogActivities),
    logActivityController.getLogActivities
  );

router.use(authSession())
  .route('/:logActivityId')
  .get(
    auth('getLogActivities'),
    validate(logActivityValidation.getLogActivity),
    logActivityController.getLogActivity
  );

export default router;

