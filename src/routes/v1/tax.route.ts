import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { taxValidation } from '../../validations';
import { taxController } from '../../controllers';
import authSession from '../../middlewares/authSession';

const router = express.Router();

router.use(authSession())
  .route('/')
  .post(
    auth('manageTaxes'),
    validate(taxValidation.createTax),
    taxController.createTax
  )
  .get(
    auth('getTaxes'),
    validate(taxValidation.getTaxes),
    taxController.getTaxes
  );

router.use(authSession())
  .route('/:taxId')
  .get(
    auth('getTaxes'),
    validate(taxValidation.getTax),
    taxController.getTax
  )
  .patch(
    auth('manageTaxes'),
    validate(taxValidation.updateTax),
    taxController.updateTax
  )
  .delete(
    auth('manageTaxes'),
    validate(taxValidation.deleteTax),
    taxController.deleteTax
  );

export default router;

